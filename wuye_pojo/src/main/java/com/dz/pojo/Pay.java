package com.dz.pojo;

public class Pay {
    /*缴费周期表*/
    private int id;
    private int yearno;/*年限*/
    private String year; /*周期*/

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getYearno() {
        return yearno;
    }

    public void setYearno(int yearno) {
        this.yearno = yearno;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }



}
